#include <iostream>

using namespace std;

int main()
{
    int a[]={8,-1,4,10,11};
    int n=5;
    for(int i=0;i<n-1;i++)
    {
        for(int j=0;j<n-1-i;j++)
        {
            if(a[j]>a[j+1]) 
            {
                int temp=a[j];
                a[j]=a[j+1];
                a[j+1]=temp;
            }
        }
    }
    for(int i=0;i<5;i++){
        cout<<a[i]<<" ";
    }
   
    return 0;
}

